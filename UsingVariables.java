class UsingVariable{
	public static void main(String[] args){
		int roomNumber;
		double doubleValue;
		String stringValue;
		
		roomNumber = 113;
		doubleValue = 2.71828;
		stringValue = "Computer Science";
		
		System.out.println("This is room #" + roomNumber);
		System.out.println("e is close to " + doubleValue);
		System.out.println("I am learning a bit about " + stringValue);
	}
}